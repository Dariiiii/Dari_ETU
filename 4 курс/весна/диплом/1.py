from sumy.parsers.plaintext import PlaintextParser
from sumy.nlp.tokenizers import Tokenizer
from sumy.summarizers.text_rank import TextRankSummarizer

def extract_main_idea(text, compression_ratio=0.3):
    parser = PlaintextParser.from_string(text, Tokenizer("russian"))
    summarizer = TextRankSummarizer()
    
    num_sentences = int(len(parser.document.sentences) * compression_ratio)
    summary = summarizer(parser.document, num_sentences)
    
    return " ".join(str(sentence) for sentence in summary)

text = """(1)Приводимая в работе методика ловли слонов позволяет снизить наносимый промысловым ловом экологический ущерб. (2)Так как снасти, используемые в рамках методики, выполнены из дерева, то даже при обрыве лески загрязнение тундры будет минимально, чего нельзя сказать об аналогичных решениях, использующих в качестве материала пластик, резину и металл. (3)Помимо этого, экологический ущерб может быть дополнительно снижен, если использовать в качестве конструкционного материала дерево без защитных покрытий и пропиток, так как цикл использования одной верши много меньше, чем среднее время разложения незащищенной древесины в условиях Крайнего Севера. (4)В качестве крепежей используются деревянные шканты диаметром 10 мм. (5)Кроме высокой экологичности, показатели экономической  эффективности предлагаемой методики превышают аналогичные значения рассмотренных аналогов. (6)Так, по результатам численного моделирования решений уравнения Э. Лефанта в течении десяти дней при нормальных условиях показатель эффективности составил 44%, что выше результатов аналогичного моделирования для блесен и воблеров."""

result = extract_main_idea(text)
print("Текст без воды:", result)
